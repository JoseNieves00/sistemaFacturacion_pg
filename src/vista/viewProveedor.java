    private void btnModificarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarPActionPerformed
        // TODO add your handling code here:
        frmModificarP.setVisible(false);
        String nit = txtNitPM.getText().trim();

        // Crear un nuevo objeto proveedor con los datos modificados
        String nombre = txtNombrePM.getText().trim();
        String contacto = txtContactoPM.getText().trim();

        if (nombre.isEmpty() || contacto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        Proveedor proveedorModificado = new Proveedor(nombre, nit, contacto);

        // Llamar al controlador para modificar el proveedor
        try {
            controladorProveedor.modificarProveedor(nit, proveedorModificado);
            cargarTablaProveedores(); // Actualizar la tabla con los cambios
            limpiarFormulario(); // Limpiar los campos
            JOptionPane.showMessageDialog(this, "Proveedor modificado exitosamente.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al modificar el proveedor: " + e.getMessage());
        }
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }//GEN-LAST:event_btnModificarPActionPerformed

