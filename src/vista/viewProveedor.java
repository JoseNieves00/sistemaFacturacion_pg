    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        // Obtener el NIT del proveedor seleccionado
        String nit = txtNitPM.getText().trim();

        // Confirmar con el usuario si desea eliminar el proveedor
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro de que desea eliminar este proveedor?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION
        );

        if (opcion == JOptionPane.YES_OPTION) {
            try {
                // Llamar al controlador para eliminar el proveedor
                controladorProveedor.eliminarProveedor(nit);

                // Actualizar la tabla después de eliminar
                cargarTablaProveedores();

                // Limpiar los campos del formulario
                limpiarFormulario();
                JOptionPane.showMessageDialog(this, "Proveedor eliminado exitosamente.");
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar el proveedor: " + e.getMessage());
            }
        }
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }//GEN-LAST:event_btnEliminarActionPerformed

