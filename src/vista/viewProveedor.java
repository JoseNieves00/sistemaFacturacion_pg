    private void btnAgregarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombreP.getText().trim();
        String nit = txtNitP.getText().trim();
        String contacto = txtContactoP.getText().trim();
        if (nombre.isEmpty() || nit.isEmpty() || contacto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        Proveedor nuevoProveedor = new Proveedor(nombre, nit, contacto);

        try {
            controladorProveedor.agregarProveedor(nuevoProveedor);
            JOptionPane.showMessageDialog(this, "Proveedor agregado exitosamente.");
            cargarTablaProveedores();
            limpiarFormulario();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el proveedor: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarPActionPerformed

